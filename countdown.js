// üõ† Cambiar aqu√≠ la fecha para hacer pruebas f√°cilmente
const targetDate = new Date(new Date().getFullYear(), 1, 14, 0, 0, 0);

function updateTimer() {
    const now = new Date();
    const timeLeft = targetDate - now;

    if (timeLeft <= 0) {
        // üü¢ Cuando llega a 0, cambia el mensaje y muestra el bot√≥n
        document.getElementById("timer").innerHTML = "üéâ Sorpresa lista üéâ";
        document.getElementById("surprise-btn").style.display = "block"; // Mostrar bot√≥n
        return;
    }

    // Convertir el tiempo restante en d√≠as, horas, minutos y segundos
    const days = Math.floor(timeLeft / (1000 * 60 * 60 * 24));
    const hours = Math.floor((timeLeft % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((timeLeft % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((timeLeft % (1000 * 60)) / 1000);

    // Mostrar en el HTML con cada unidad en su l√≠nea
    document.getElementById("timer").innerHTML = `
        <div class="time-box">${days}<span>D√≠as</span></div>
        <div class="time-box">${hours}<span>Horas</span></div>
        <div class="time-box">${minutes}<span>Minutos</span></div>
        <div class="time-box">${seconds}<span>Segundos</span></div>
    `;
}

// Actualizar cada segundo
setInterval(updateTimer, 1000);
updateTimer();

